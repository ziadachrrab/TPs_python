def dollars_to_dirhams(dollars):
    """Convertit une somme en dollars en dirhams.
    
    :param dollars: Montant en dollars (float).
    :return: Montant en dirhams (float).
    """
    taux_conversion = 10.0  # Exemple : 1 dollar = 10 dirhams
    return dollars * taux_conversion

def meters_to_kilometers(meters):
    """Convertit une distance en mètres en kilomètres.
    
    :param meters: Distance en mètres (float).
    :return: Distance en kilomètres (float).
    """
    return meters / 1000.0
